package Clases;

public class cListViewColumns {
    public String nombre;
    public String imagen;
    public String descripcion;
    public String info;

    public cListViewColumns(String nombre, String imagen, String descripcion, String info){
        this.nombre = nombre;
        this.imagen = imagen;
        this.descripcion = descripcion;
        this.info = info;
    }
}
